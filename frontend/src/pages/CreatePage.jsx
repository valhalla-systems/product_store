import {
  Box,
  Button,
  Container,
  Heading,
  Image,
  Input,
  useColorModeValue,
  useToast,
  VStack,
} from "@chakra-ui/react";
import { useRef, useState } from "react";
import { useProductStore } from "../store/product";

/**
 * P√°gina de cria√ß√£o de novos produtos.
 * Agora com pr√©-visualiza√ß√£o da imagem e limpeza completa ap√≥s o envio.
 */
const CreatePage = () => {
  const [newProduct, setNewProduct] = useState({
    name: "",
    price: "",
    image: "",
  });

  const [preview, setPreview] = useState(null); // üñºÔ∏è Estado para exibir a imagem
  const [isLoading, setIsLoading] = useState(false);

  const toast = useToast();
  const { createProduct } = useProductStore();

  const fileInputRef = useRef(null);

  const handleAddProduct = async () => {
    setIsLoading(true);

    const { success, message } = await createProduct(newProduct);

    toast({
      title: success ? "Sucesso ‚úÖ" : "Erro ‚ùå",
      description: success
        ? `O produto "${newProduct.name}" foi criado com sucesso!`
        : message,
      status: success ? "success" : "error",
      duration: 3000,
      isClosable: true,
    });

    if (success) {
      // üßπ Limpa os campos e o preview ap√≥s o envio
      setNewProduct({ name: "", price: "", image: "" });
      setPreview(null);

      if (fileInputRef.current) {
        fileInputRef.current.value = "";
      }
    }

    setIsLoading(false);
  };

  const handleImageChange = (e) => {
    const file = e.target.files[0];

    if (file) {
      // Cria um preview tempor√°rio com URL local
      setPreview(URL.createObjectURL(file));
      setNewProduct({ ...newProduct, image: file });
    } else {
      setPreview(null);
      setNewProduct({ ...newProduct, image: "" });
    }
  };

  return (
    <Container maxW="container.sm">
      <VStack spacing={8}>
        <Heading as="h1" size="2xl" textAlign="center" mb={8}>
          Criar Novo Produto
        </Heading>

        <Box
          w="full"
          bg={useColorModeValue("white", "gray.800")}
          p={6}
          rounded="lg"
          shadow="md"
        >
          <VStack spacing={4}>
            {/* Campo: Nome do produto */}
            <Input
              placeholder="Nome do Produto"
              value={newProduct.name}
              onChange={(e) =>
                setNewProduct({ ...newProduct, name: e.target.value })
              }
            />

            {/* Campo: Pre√ßo */}
            <Input
              placeholder="Pre√ßo"
              type="number"
              value={newProduct.price}
              onChange={(e) =>
                setNewProduct({ ...newProduct, price: e.target.value })
              }
            />

            {/* Campo: Upload da Imagem */}
            <Input
              type="file"
              ref={fileInputRef}
              accept="image/*"
              onChange={handleImageChange}
            />

            {/* üñºÔ∏è Pr√©-visualiza√ß√£o da Imagem */}
            {preview && (
              <Image
                src={preview}
                alt="Pr√©-visualiza√ß√£o"
                maxH="200px"
                borderRadius="md"
                objectFit="cover"
                shadow="sm"
              />
            )}

            {/* Bot√£o de envio */}
            <Button
              colorScheme="blue"
              onClick={handleAddProduct}
              w="full"
              isLoading={isLoading}
              loadingText="Adicionando..."
              isDisabled={
                !newProduct.name ||
                !newProduct.price ||
                !newProduct.image ||
                isLoading
              }
            >
              Adicionar Produto
            </Button>
          </VStack>
        </Box>
      </VStack>
    </Container>
  );
};

export default CreatePage;
